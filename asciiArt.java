class asciiArt {
    public static void main(String[] args) {
      String initials = "rl";
      System.out.printf("\nMy initials are %s.\n\n",initials);  
      System.out.printf("\nMy initials are %S.\n\n",initials);  
      System.out.printf("\nMy initials are %6S.\n\n",initials);  
      System.out.printf("\nMy initials are %-6S.\n\n",initials);  
      System.out.printf("\nOur dinner bill was %f.\n\n",125.50);  
      System.out.printf("\nOur dinner bill was $%.2f.\n\n",125.50);  
      System.out.printf("\nOur dinner bill was $%10.2f.\n\n",125.50);  

      System.out.println(" o__ __o         o");
      System.out.println("<|     v\\       <|> ");
      System.out.println("/ \\     <\\      / \\");
      System.out.println("\\o/     o/      \\o/ ");
      System.out.println(" |__  _<|        |  ");
      System.out.println(" |       \\      / \\ ");
      System.out.println("<o>       \\o    \\o/ ");
      System.out.println(" |         v\\    |  ");
      System.out.println("/ \\         <\\  / \\ _\\o__/_");
      System.out.println();

//   o__ __o         o          
//  <|     v\       <|>         
//  / \     <\      / \         
//  \o/     o/      \o/         
//   |__  _<|        |          
//   |       \      / \         
//  <o>       \o    \o/         
//   |         v\    |          
//  / \         <\  / \ _\o__/_ 
                             
                             
                             

    }// end main
}// end class